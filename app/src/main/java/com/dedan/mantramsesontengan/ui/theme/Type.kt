enum class TypographySize{
    NORMAL,
    SMALL,
    LARGE
}

// Selector function
@Composable
fun Typography(size: TypographySize): Typography {
    return when (size) {
        TypographySize.NORMAL -> NormalTypography
        TypographySize.SMALL -> SmallTypography
        TypographySize.LARGE -> LargeTypography
    }
}